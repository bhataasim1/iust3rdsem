#include <iostream>

using namespace std;

double power (double dd, int p = 1)
{
    double power = 1;
    for ( int i = 0; i <= p; i++)
    power *= dd;
    return power;
}
char power ( char ch, int p = 1)
{
    char power = 1;
    for (int i = 0; i <= p; i++)
    power *= ch;
    return power;
}

int power (int a , int p = 1)
{
    int power = 1;
    for ( int i = 0; i <= p; i++)
    power *= a;
    return power;
}
long power (long l , int p = 1)
{
    long power = 1;
    for ( int i = 0; i <= p; i++)
    power *= l;
    return power;
}
float power (float f , int p = 1)
{
    float power = 1;
    for ( int i = 0; i <= p; i++)
    power *= f;
    return power;
}

int main()
{
    double num1;
    char num2;
    int num3;
    long num4;
    float num5;
    
    cout << "Enter the Double Value : ";
    cin >> num1;
    cout << "power of " << num1 << " is " << power(num1) << endl;
    
    cout << "Enter the Character Value : ";
    cin >> num2;
    cout << "power of " << num2 << " is " << power(num2) << endl;
    
    cout << "Enter the Integer Value : ";
    cin >> num3;
    cout << "power of " << num3 << " is " << power(num3) << endl;
    
    cout << "Enter the Long Value : ";
    cin >> num4;
    cout << "power of " << num4 << " is " << power(num4) << endl;
    
    cout << "Enter the Float Value : ";
    cin >> num5;
    cout << "power of " << num5 << " is " << power(num5) << endl;

    return 0;
}
